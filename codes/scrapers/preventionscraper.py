def extract_prevention_methods(text, prevention_keywords):
    found_methods = [word for word in prevention_keywords if word in text.lower()]
    return ", ".join(set(found_methods)) if found_methods else None  # Remove duplicates


# Function to extract prevention-related terms using regex
def extract_prevention_methods2(text, therapy_pattern, crisis_services_pattern, medication_pattern, holistic_pattern, medical_terms_pattern):
    therapy_matches = re.findall(therapy_pattern, text, flags=re.IGNORECASE)
    crisis_matches = re.findall(crisis_services_pattern, text, flags=re.IGNORECASE)
    medication_matches = re.findall(medication_pattern, text, flags=re.IGNORECASE)
    holistic_matches = re.findall(holistic_pattern, text, flags=re.IGNORECASE)
    medical_terms_matches = re.findall(medical_terms_pattern, text, flags=re.IGNORECASE)

    all_matches = list(set(therapy_matches + crisis_matches + medication_matches + holistic_matches + medical_terms_matches))  # Remove duplicates
    return ", ".join(all_matches) if all_matches else None


