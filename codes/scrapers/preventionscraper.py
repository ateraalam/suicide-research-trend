# Function to detect language and translate if necessary
def translate_to_english(text):
    try:
        detected_lang = detect(text)  # Detect language
        if detected_lang != "en":  # Only translate non-English abstracts
            translated_text = GoogleTranslator(source='auto', target='en').translate(text)
            if translated_text and translated_text.lower() != text.lower():  # Ensure translation changed the text
                return translated_text, True  # Return translated text and flag
        return text, False  # Return original text and flag
    except Exception as e:
        return text, False  # If detection or translation fails, return original


