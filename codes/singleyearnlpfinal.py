# — Cell 15: Grant–Paper Bipartite Network (PyVis) —

from pyvis.network import Network
import pandas as pd
import matplotlib.cm as cm
import matplotlib.colors as mcolors

# 1) Build a DataFrame of grant–paper edges
df['grant_information'] = df['grant_information'].fillna('').astype(str)
edges = []
for idx, grants in df['grant_information'].items():      # ← use .items() here
    for grant in grants.split(';'):
        grant = grant.strip()
        if grant and grant.lower() not in ('none', 'nan', ''):
            edges.append({'grant': grant, 'paper_id': f"Paper_{idx}"})

grant_edges_df = pd.DataFrame(edges)

# 2) Count papers per grant
grant_counts = grant_edges_df['grant'].value_counts()

# 3) Create color map & size scaling
cmap = cm.get_cmap('tab20', len(grant_counts))
grant_color_map = {
    grant: mcolors.rgb2hex(cmap(i))
    for i, grant in enumerate(grant_counts.index)
}
max_count = grant_counts.max()
grant_size_scaled = {
    grant: 10 + (count / max_count) * 40
    for grant, count in grant_counts.items()
}

# 4) Initialize PyVis network
net = Network(
    height="750px", width="100%",
    bgcolor="#ffffff", font_color="black",
    notebook=True
)

# 5) Add grant nodes
for grant, count in grant_counts.items():
    net.add_node(
        n_id=grant, label=grant,
        color=grant_color_map[grant],
        size=grant_size_scaled[grant],
        title=f"{grant} — {count} papers"
    )

# 6) Add paper nodes
for pid in grant_edges_df['paper_id'].unique():
    net.add_node(n_id=pid, label=pid, color='lightgreen', size=10)

# 7) Add edges
for _, row in grant_edges_df.iterrows():
    net.add_edge(row['grant'], row['paper_id'])

# 8) Display
net.show_buttons(filter_=['physics'])
net.show("grant_network.html")

# — Cell 16: Interactive Co‑Authorship Network via PyVis —

from pyvis.network import Network
from collections import Counter
import matplotlib.cm as cm
import matplotlib.colors as mcolors
import pandas as pd

# 1) Identify author columns and build edge list
author_cols = [c for c in df.columns if c.startswith('author_details.')]
edges = []
for _, row in df.iterrows():
    authors = [row[c] for c in author_cols if pd.notna(row[c]) and row[c] != '']
    for i in range(len(authors)):
        for j in range(i+1, len(authors)):
            edges.append((authors[i], authors[j]))

# 2) Compute publication counts per author
all_authors = pd.Series(df[author_cols].values.ravel()).dropna().astype(str)
author_counts = Counter(all_authors)

# 3) Prepare node sizes (10–50 px) and colors (categorical)
max_ct = max(author_counts.values())
size_map = {a: 10 + (cnt/max_ct)*40 for a,cnt in author_counts.items()}
cmap = cm.get_cmap('tab20', len(author_counts))
color_map = {a: mcolors.rgb2hex(cmap(i)) for i,a in enumerate(author_counts)}

# 4) Initialize PyVis network
net = Network(
    height="750px",
    width="100%",
    bgcolor="#ffffff",
    font_color="black",
    notebook=True
)

# 5) Add author nodes
for author, cnt in author_counts.items():
    net.add_node(
        n_id=author,
        label=author,
        size=size_map[author],
        color=color_map[author],
        title=f"{cnt} publications"
    )

# 6) Add co‑author edges
for a, b in edges:
    net.add_edge(a, b)

# 7) Show controls and render
net.show_buttons(filter_=['physics'])
net.show("coauthorship_network.html")