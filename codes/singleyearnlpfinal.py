from wordcloud import WordCloud
import matplotlib.pyplot as plt
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.decomposition import LatentDirichletAllocation, PCA
from sklearn.cluster import KMeans
import plotly.express as px

# 1) Word Cloud of all abstracts
all_text = ' '.join(df['abstract'].dropna())
wc = WordCloud(width=800, height=400, background_color='white').generate(all_text)
plt.figure(figsize=(15, 7))
plt.imshow(wc, interpolation='bilinear')
plt.axis('off')
plt.title('Word Cloud of Abstract Texts')
plt.show()

# 2) TF‑IDF vectorization
tfidf = TfidfVectorizer(max_features=1000, stop_words='english')
X = tfidf.fit_transform(df['abstract'])

# 3) LDA topic modeling
lda = LatentDirichletAllocation(n_components=5, random_state=42)
lda.fit(X)

for idx, comp in enumerate(lda.components_, start=1):
    terms = tfidf.get_feature_names_out()
    top_terms = [terms[i] for i in comp.argsort()[-10:]]
    print(f"Topic {idx}: {top_terms}")

# 4) K‑Means clustering
kmeans = KMeans(n_clusters=5, random_state=42)
df['cluster'] = kmeans.fit_predict(X)

# 5) PCA for 2D visualization
pca = PCA(n_components=2, random_state=42)
coords = pca.fit_transform(X.toarray())
df['pca1'], df['pca2'] = coords[:, 0], coords[:, 1]

fig = px.scatter(
    df,
    x='pca1',
    y='pca2',
    color=df['cluster'].astype(str),
    title='Abstract Clusters (KMeans + PCA)'
)
fig.show()

