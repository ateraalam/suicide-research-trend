# — Cell 13: Emotion Proportions by Journal —

import pandas as pd
import seaborn as sns
import matplotlib.pyplot as plt
from nrclex import NRCLex

# Define the eight NRC emotion columns
emotion_cols = [
    'anger', 'anticipation', 'disgust', 'fear',
    'joy', 'sadness', 'surprise', 'trust'
]

# 0) Compute row‑level emotion scores (if not already present)
#    This builds a small DF of shape (n_rows × 8)
row_emotions = (
    df['abstract']
      .fillna('')
      .astype(str)
      .apply(lambda text: pd.Series(NRCLex(text).raw_emotion_scores))
      .fillna(0)
      .astype(int)
)

# 1) Concatenate these back onto df
df_em = pd.concat([df, row_emotions[emotion_cols]], axis=1)

# 2) Re‑split into prolific vs non‑prolific rows
author_cols      = [c for c in df_em.columns if c.startswith('author_details.')]
mask_prolific    = df_em[author_cols].isin(prolific.keys()).any(axis=1)
prol_em_df       = df_em.loc[mask_prolific]
non_prol_em_df   = df_em.loc[~mask_prolific]

# 3) Sum emotions per journal
emo_pro = prol_em_df.groupby('journal_name')[emotion_cols].sum()
emo_non = non_prol_em_df.groupby('journal_name')[emotion_cols].sum()

# 4) Keep only journals with ≥3 papers in both groups
cnt_pro = prol_em_df['journal_name'].value_counts()
cnt_non = non_prol_em_df['journal_name'].value_counts()
common  = cnt_pro[cnt_pro >= 3].index.intersection(cnt_non[cnt_non >= 3].index)

emo_pro = emo_pro.loc[common]
emo_non = emo_non.loc[common]

# 5) Convert sums to percentages
prop_pro = emo_pro.div(cnt_pro, axis=0).loc[common] * 100
prop_non = emo_non.div(cnt_non, axis=0).loc[common] * 100

# 6) Plot side‑by‑side heatmaps
fig, axes = plt.subplots(1, 2, figsize=(16, 8), sharey=True)

sns.heatmap(
    prop_pro, ax=axes[0],
    cmap='YlGnBu', annot=False, fmt=".1f",
    cbar_kws={'label':'% of Emotions'}
)
axes[0].set_title('Emotion % by Journal (Prolific)')
axes[0].set_xlabel('Emotion')
axes[0].set_ylabel('Journal')

sns.heatmap(
    prop_non, ax=axes[1],
    cmap='YlGnBu', annot=False, fmt=".1f",
    cbar_kws={'label':'% of Emotions'}
)
axes[1].set_title('Emotion % by Journal (Non‑Prolific)')
axes[1].set_xlabel('Emotion')
axes[1].set_ylabel('')

plt.tight_layout()
plt.show()

