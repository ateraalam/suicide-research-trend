# — Cell 8: Emotion Detection per Prolific Author —

from nrclex import NRCLex
import pandas as pd
import matplotlib.pyplot as plt
from collections import Counter

# 1) Rebuild an author-level table with combined abstracts
records = []
author_cols = [c for c in df.columns if c.startswith('author_details.')]

for author, count in prolific.items():
    # find all rows where this author appears
    mask = df[author_cols].eq(author).any(axis=1)
    abstracts = df.loc[mask, 'abstract'].dropna().astype(str)
    combined = ' '.join(abstracts)
    records.append({
        'author': author,
        'publications': count,
        'text': combined
    })

author_df = pd.DataFrame(records)

# 2) Emotion extraction function
